                         <h2>A new dawn for controlling AC slip ring motors</h2>
                         <h3 id="sub-h3">With 2 Quadrant and 4 Quadrant models, you can use the JL/X to control AC slip ring motors up to 975kW, with all models available from stock.</h3>
                         <p id="sub-p">The JL/X series AC Drive is a fully digital drive system providing 45kW – 975kW output power to control AC slip ring motors. The JL/X is built on the same platform as the PL/X and provides identical controls. The JL/X is designed for retrofit applications on cranes, material handling, hoists, pumps, compressors & conveyors.</p>
                         <div class="img">
                             <img src="./img/cranes-hoists.jpg" alt="">
                         </div>
                         <h1>IDEAL UPGRADE FOR CRANES & HOISTS</h1>
                         <p>Upgrading your AC-slip ring motor application usually involves replacing the motor with a new motor and drive combination (expensive), or modifying the existing motor to allow a retrofit standard AC Drive. For users wishing to maintain the high starting torque of AC slip ring motors, you can simply bolt on the JL/X AC Slip-Ring Drive, which is designed to work seamlessly with AC slip ring motors. The JL/X provides full digital control and maintains the classic torque-speed curve so critical for lifting applications.</p>
                         
                         <div class="img  right">
                            <img src="./img/compact-versatile-design.jpg" alt="">
                         </div>
                         <h1 class="left">COMPACT & VERSATILE DESIGN</h1>
                         <p id="left">Available in 2Q (single direction) and 4Q (fully regenerative) variants, the range comprises 3 compact chassis sizes with models rated at 150% overload (standard) or 250% overload (high duty). The JL/X boasts an extremely versatile operating input voltage range of 12V – 500VAC as standard. Options are available for 600V and 690VAC input.</p>
                         <div class="img left2">
                                <img src="./img/Easy-to-configure.jpg" alt="">
                         </div> 
                         <div>
                            <h1>EASY TO CONFIGURE  </h1> 
                            <p>All models include a 40-character alphanumeric backlit display that makes it easier to quickly navigate through an extensive range of software functions. The JL/X is fitted with 17 digital inputs and 8 analogue inputs as standard, with 7 digital outputs and 4 analogue outputs.
                               The JL/X is fully configurable by the user and comes equipped, as standard, with a comprehensive set of function blocks that allow easy set-up and control of AC Slip Ring motors. The JL/X also includes PL Pilot+, a windows based, configuration and monitoring software package. The JL/X is also available with powerful inbuilt smart automation. Drive.Web distributed control technology uses Ethernet and powerful graphical tools to provide robust Programmable Peer Control (PPC) for drives and systems. Options for field bus systems include Ethernet, Profibus, CANbus, Modbus, DeviceNet and many more.
                               All frame sizes are CE approved. </p> 
